潮水退的时候，人们才看清水下暗礁。安然（Enron）和世通（WorldCom）相继因重大财务造假而破产。

随着高科技股市的走低，湾区的房地产价格也开始缓慢下跌。

雨峤家的后院，长着一棵茂密的无花果。无花果树旁边，是一株参天的中华榆，夏天时浓荫覆盖，榆枝摇曳。它虽然没有漂亮的花朵，却是坚实又抗虫害的大树，秋天时会散落一地的榆钱。

